There are users
Each user can belong to one or more teams
Each team has sprints
For each sprint, members of the team can create cards with the actions they want to mention in the sprint retrospective
These cards can be organized into 3 preset categories: "What went well", "Stop doing" and "Continue doing"
Cards can be moved between categories
Cards can be edited and deleted
Cards can be voted by members of the team
Members of the team can comment on cards
